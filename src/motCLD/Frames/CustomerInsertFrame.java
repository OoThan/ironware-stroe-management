    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        String Cust_Name=txtCust_Name.getText();
        String Phone_No=txtPhone_No.getText();
        String Address=txtAddress.getText();
        List list=new List();
        String errorText="";
       // double price=Double.parseDouble(txtPrice.getText());
        if(Cust_Name.equals("") || Phone_No.equals("") || Phone_No.equals("Enter Phone Number Only!") || Address.equals("") ){
            errorLable.setForeground(Color.red);
            errorText+="       Any field has Empty!";
            errorLable.setText(errorText);
        }else{
            
            
            try{
                int flag=0;
                Class.forName("com.mysql.jdbc.Driver");
                con = (Connection)DriverManager.getConnection(url,login,pass);
                Statement stmt=con.createStatement();
                ResultSet dataRs = stmt.executeQuery("select Cust_Name from Customers");
                String str="";
                while(dataRs.next()){
                    if(Cust_Name.equals(dataRs.getString(1).toString())){
                        flag=1;
                    }  
                    System.out.println(Cust_Name+" "+dataRs.getString(1).toString());
                }
                System.out.println(flag);
                if(flag!=1){
                    PreparedStatement pres=null;
                    String query="insert Customers(Cust_Name,Phone_No,Address,Debt,Payment,Total_Purchased_Charged) values(?,?,?,?,?,?)";
                    pres=con.prepareStatement(query);
                    pres.setString(1, Cust_Name);
                    pres.setString(2, Phone_No);
                    pres.setString(3, Address);
                    pres.setInt(4, 0);
                    pres.setInt(5, 0);
                    pres.setInt(6, 0);
                    pres.execute();
                    //JOptionPane.showMessageDialog(rootPane, "Inserted Successfully!");
                   // errorLable.setForeground(Color.YELLOW);
                   errorLable.setForeground(Color.GREEN);
                    errorLable.setText("       Customer Inserted Successfully!");
                    con.close();
                }else if(flag==1){
                    errorLable.setForeground(Color.YELLOW);
                    errorLable.setText("       "+Cust_Name+" is already exit in database!");
                }
                }catch (SQLException ex) {
                    Logger.getLogger(CustomerInsertFrame.class.getName()).log(Level.SEVERE, null, ex);
                } catch (Exception ex) {
                Logger.getLogger(CustomerInsertFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            } 
    }//GEN-LAST:event_btnInsertActionPerformed

