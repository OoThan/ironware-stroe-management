     public ArrayList<Customers> getCustList() throws Exception{
        
        ArrayList<Customers> custList=new ArrayList<Customers>();
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection)DriverManager.getConnection(url,login,pass);
            String query="select Cust_Name from Customers";
            Statement stmt;
            ResultSet rs;
        try {
            stmt=con.createStatement();
            rs=stmt.executeQuery(query);
            Customers cust;
            
            while (rs.next()) {
                cust=new Customers(rs.getString("Cust_Name"));
                custList.add(cust);
            }
            //con.close();
        } catch (SQLException ex) {
            Logger.getLogger(CustomersControlFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        return custList;
    }
     
     
